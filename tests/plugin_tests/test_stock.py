import pytest

from plugins import stock


@pytest.mark.parametrize(
    "number,output",
    [
        (0, "0.00"),
        (1, "1.00"),
        (10, "10.00"),
        (100, "100.00"),
        (1000, "1,000.00"),
        (10000, "10,000.00"),
        (100000, "100,000.00"),
        (1000000, "1.00M"),
        (10000000, "10.00M"),
        (100000000, "100.00M"),
        (1000000000, "1.00B"),
        (10000000000, "10.00B"),
        (100000000000, "100.00B"),
        (1000000000000, "1.00T"),
        (8676423467886, "8.68T"),
        (-0.000001234, "-0.00"),
    ],
)
def test_format_money(number, output):
    assert stock.format_money(number) == output
